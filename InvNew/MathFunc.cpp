double Pythag(double a, double b)
{
	double absa,absb;
	absa=fabs(a);
	absb=fabs(b);
	if (absa > absb) return absa*sqrt( 1.0 + pow(absb/absa, 2) );
	else return (absb == 0.0 ? 0.0 : absb*sqrt( 1.0 + pow(absa/absb, 2) ));
}

